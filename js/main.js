var loading_console_output = document.querySelector(".loading_console_output");

function typeText(element, text, slowInterval, fastInterval) {
    const elementsToInsertImmediately = ["<br>", "<span class='loading_red'>", "<span class='loading_orange'>", "<span class='loading_green'>", "<span class='loading_blue'>"];
    const lines = text.split('<br>');
    let i = 0;
    let interval = fastInterval;

    function type() {
        if (i < lines.length) {
            let line = lines[i];
            let inserted = false;
            for (const elem of elementsToInsertImmediately) {
                if (line.includes(elem)) {
                    element.innerHTML += line + '<br>';
                    i++;
                    inserted = true;
                    break;
                }
            }
            if (!inserted) {
                element.innerHTML += line + '<br>';
                i++;
            }
            if (line.includes("Decryption in progress...")) {
                setTimeout(() => {
                    interval = slowInterval;
                    type();
                }, 1000);
                return;
            } else if (line.includes("/// Decryption Complete ///")) {
                interval = fastInterval;
            }
            setTimeout(type, interval);
        }
    }
    type();
}

